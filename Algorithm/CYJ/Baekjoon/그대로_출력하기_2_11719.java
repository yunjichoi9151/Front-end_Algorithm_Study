package Baekjoon;
import java.util.Scanner;

public class 그대로_출력하기_2_11719 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(sc.hasNextLine()) {
            String s = sc.nextLine();
            System.out.println(s);
        }
        sc.close();
    }
}
